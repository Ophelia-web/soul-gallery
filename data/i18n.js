export function uiKeysMatch() {
  const zhKeys = Object.keys(UI.zh).sort();
  const enKeys = Object.keys(UI.en).sort();
  return (
    zhKeys.length === enKeys.length &&
    zhKeys.every((key, index) => key === enKeys[index])
  );
}
